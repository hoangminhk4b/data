package com.example.hoangminhk4b.project.Model;

public class RecycleViewIconHomeModel {
    private String title;
    private int avatar;

    public RecycleViewIconHomeModel(String title, int avatar) {
        this.title = title;
        this.avatar = avatar;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getAvatar() {
        return avatar;
    }

    public void setAvatar(int avatar) {
        this.avatar = avatar;
    }
}
